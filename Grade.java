class Grade {
    public static void main(String[] args) {

        int mark =98;

        if(mark >= 90)
            System.out.println("O");
        else if(mark >= 80)
            System.out.println("E");
        else if(mark >= 70)
            System.out.println("A");
        else if(mark >= 60)
            System.out.println("B");
        else if(mark >= 50)
            System.out.println("C");
        else
            System.out.println("Fail");
    }
}
